data class ChatMessage(
    val text: String,
    val isUser: Boolean,
    val isTyping: Boolean = false
)

@Composable
fun OnboardingScreen(
    onComplete: () -> Unit
) {
    val context = LocalContext.current
    val onboardingPrefs = remember { OnboardingPreferences(context) }
    val languagePrefs = remember { LanguagePreferences(context) }
    val currentLanguage = rememberCurrentLanguage()
    val scope = rememberCoroutineScope()
    val questionGenerator = remember { OnboardingQuestionGenerator() }
    
    var userName by remember { mutableStateOf("") }  // User's name
    var showNameInput by remember { mutableStateOf(true) }  // Show name input first
    var currentQuestionIndex by remember { mutableStateOf(0) }
    var messages by remember { mutableStateOf(listOf<ChatMessage>()) }
    var showHoroscope by remember { mutableStateOf(false) }
    var isAgentTyping by remember { mutableStateOf(false) }
    var questions by remember { mutableStateOf<List<OnboardingQuestion>>(emptyList()) }
    var isAiGenerated by remember { mutableStateOf(false) }  // Track if questions are AI-generated
    var userAnswers by remember { mutableStateOf<Map<String, String>>(emptyMap()) }
    var displayedText by remember { mutableStateOf("") }  // For typing animation
    
    // Database instance
    val database = remember { AppDatabase.getDatabase(context) }
    
    val listState = rememberLazyListState()
    
    // Load questions from Gemini API
    LaunchedEffect(currentLanguage) {
        try {
            questionGenerator.generateQuestions(currentLanguage).collect { (generatedQuestions, aiGenerated) ->
                questions = generatedQuestions
                isAiGenerated = aiGenerated
            }
        } catch (e: Exception) {
            android.util.Log.e("OnboardingScreen", "Error loading questions", e)
            // Use fallback questions if API fails
            questions = MockData.onboardingQuestions
            isAiGenerated = false
        }
    }
    
    // Handle onboarding completion
    val handleComplete: () -> Unit = {
        scope.launch {
            // Save user to database
            val userId = UUID.randomUUID().toString()
            val userEntity = UserEntity(
                id = userId,
                name = userName.ifEmpty { "User" },  // Default to "User" if empty
                phone = "",  // Can be added later
                isOnboarded = true,
                language = "hi"  // Default to Hindi
            )
            database.userDao().upsertUser(userEntity)
            
            // Mark onboarding as complete
            onboardingPrefs.setOnboardingComplete(true)
        }
        onComplete()  // Navigate to home (call immediately, not in coroutine)
    }
    
    // Handle name submission
    val handleNameSubmit: (String) -> Unit = { name ->
        if (name.isNotBlank()) {
            userName = name.trim()
            showNameInput = false
        }
    }
    
    // Show greeting and first question after name is entered
    LaunchedEffect(showNameInput, questions.size) {
        if (!showNameInput && questions.isNotEmpty() && messages.isEmpty()) {
            delay(500)
            
            // Generate personalized greeting from Gemini
            val greeting = try {
                questionGenerator.generateGreeting(userName, currentLanguage)
            } catch (e: Exception) {
                android.util.Log.e("OnboardingScreen", "Error generating greeting", e)
                if (userName.isNotEmpty()) {
                    "Namaste $userName! 🙏 Main Nischint hoon, tera financial buddy!"
                } else {
                    "Namaste! 🙏 Main Nischint hoon, tera financial buddy!"
                }
            }
            
            // Show typing indicator
            isAgentTyping = true
            delay(800)
            isAgentTyping = false
            
            // Type greeting character by character (Hindi typing style)
            displayedText = ""
            for (i in greeting.indices) {
                displayedText = greeting.substring(0, i + 1)
                delay(50)  // Typing speed for Hindi characters
            }
            
            messages = messages + ChatMessage(
                text = displayedText,
                isUser = false
            )
            displayedText = ""
            
            // Wait a bit, then show first question
            delay(1000)
            
            if (questions.isNotEmpty()) {
                // Show typing indicator
                isAgentTyping = true
                delay(800)
                isAgentTyping = false
                
                // Type first question character by character
                val firstQuestion = questions[0].text
                displayedText = ""
                for (i in firstQuestion.indices) {
                    displayedText = firstQuestion.substring(0, i + 1)
                    delay(50)  // Typing speed for Hindi
                }
                
                messages = messages + ChatMessage(
                    text = displayedText,
                    isUser = false
                )
                displayedText = ""
            }
        }
    }
    
    // Scroll to bottom when new messages added
    LaunchedEffect(messages.size) {
        if (messages.isNotEmpty()) {
            listState.animateScrollToItem(messages.size - 1)
        }
    }
    
    Column(
        modifier = Modifier
            .fillMaxSize()
            .background(
                Brush.verticalGradient(
                    colors = listOf(Background, Surface)
                )
            )
    ) {
        // Header
        OnboardingHeader()
        
        // Chat Messages
        LazyColumn(
            state = listState,
            modifier = Modifier
                .weight(1f)
                .fillMaxWidth()
                .padding(horizontal = 16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp),
            contentPadding = PaddingValues(vertical = 16.dp)
        ) {
            items(messages) { message ->
                ChatBubble(message = message)
            }
            
            // Show typing animation text if currently typing
            if (displayedText.isNotEmpty()) {
                item {
                    ChatBubble(message = ChatMessage(text = displayedText, isUser = false, isTyping = true))
                }
            } else if (isAgentTyping) {
                // Typing indicator (only when not showing typed text)
                item {
                    TypingIndicator()
                }
            }
        }
        
        // Name Input, Options, or Horoscope
        AnimatedContent(
            targetState = Triple(showNameInput, showHoroscope, currentQuestionIndex),
            transitionSpec = {
                fadeIn() + slideInVertically { it } togetherWith fadeOut() + slideOutVertically { -it }
            },
            label = "bottom_content"
        ) { (showingNameInput, showingHoroscope, _) ->
            when {
                showingNameInput -> {
                    // Name Input Panel
                    NameInputPanel(
                        onNameSubmit = handleNameSubmit
                    )
                }
                showingHoroscope -> {
                    HoroscopeCard(
                        horoscope = MockData.horoscope,
                        onContinue = handleComplete
                    )
                }
                currentQuestionIndex < questions.size && questions.isNotEmpty() -> {
                    val currentQuestion = questions[currentQuestionIndex]
                    OptionsPanel(
                    options = currentQuestion.options,
                    isAiGenerated = currentQuestion.isAiGenerated,  // Pass AI flag
                    onOptionSelected = { option ->
                        // Add user message
                        messages = messages + ChatMessage(text = option, isUser = true)
                        
                        // Store answer
                        userAnswers = userAnswers + (questions[currentQuestionIndex].id to option)
                        
                        // Move to next question or show horoscope
                        if (currentQuestionIndex < questions.size - 1) {
                            currentQuestionIndex++
                            // The next question will be added via LaunchedEffect
                        } else {
                            // Generate completion message
                            scope.launch {
                                val completionMsg = questionGenerator.generateCompletionMessage(userAnswers)
                                isAgentTyping = true
                                delay(1000)
                                isAgentTyping = false
                                
                                // Type completion message
                                displayedText = ""
                                for (i in completionMsg.indices) {
                                    displayedText = completionMsg.substring(0, i + 1)
                                    delay(50)
                                }
                                messages = messages + ChatMessage(text = displayedText, isUser = false)
                                displayedText = ""
                                
                                delay(1500)
                                showHoroscope = true
                            }
                        }
                    },
                    enabled = !isAgentTyping && displayedText.isEmpty()
                    )
                }
            }
        }
    }
    
    // Handle showing next question with typing animation
    LaunchedEffect(currentQuestionIndex) {
        if (currentQuestionIndex > 0 && currentQuestionIndex < questions.size && questions.isNotEmpty()) {
            delay(800)
            isAgentTyping = true
            delay(800)
            isAgentTyping = false
            
            // Type question character by character (Hindi typing style)
            val currentQuestion = questions[currentQuestionIndex]
            val questionText = if (currentQuestion.isAiGenerated) {
                "${currentQuestion.text} *"  // Add '*' for AI-generated questions
            } else {
                currentQuestion.text
            }
            displayedText = ""
            for (i in questionText.indices) {
                displayedText = questionText.substring(0, i + 1)
                delay(50)  // Typing speed for Hindi characters
            }
            
            messages = messages + ChatMessage(
                text = displayedText,
                isUser = false
            )
            displayedText = ""
        }
    }
}

